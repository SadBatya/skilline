  },
  methods: {
    visibilityChanged(flag) {
      if (!flag) {
        return;
      }
      const rows = this.$refs.iconRows;
      if (!rows) {
        return;
      }
      clearTimeout(this.animationStart);
      const rowElements = rows.querySelectorAll('.icons__icon');
      const countTime = 100;
      rowElements.forEach((el, index) => {
        this.animationStart = setTimeout(() => {
          el.classList.add('_stop')
        }, countTime * (index + 1));
      });
    }
  }
}
