import person5 from '@/static/img/portfolio/pf-levelhome/client-level-home/client-level-home-person-5.jpg'

export default {
  name: 'ClientLevelHome',
  data() {
    return {
      images: [
        { src: person1, alt: 'Person 1' },
        { src: person2, alt: 'Person 2' },
        { src: person3, alt: 'Person 3' },
        { src: person4, alt: 'Person 4' },
        { src: person5, alt: 'Person 5' },
      ],
      currentIndex: 0,
      animationDuration: 500, // Продолжительно анимации
      cycleDelay: 1, // Задержка между анимациями
      overallDelay: 500, // Задержка после полного цикла анимации
    }
  },
  mounted() {
    this.animateCycle(0)
  },
  methods: {
    animateCycle(index) {
      const cards = document.querySelectorAll('.levelhome_box-img_inner')
      if (index >= cards.length) {
        setTimeout(() => {
          this.animateCycle(0)
        }, this.overallDelay)
        return
      }

      const currentCard = cards[index]
      currentCard.classList.add('animated')

      setTimeout(() => {
        currentCard.classList.remove('animated')
        currentCard.classList.add('stopped')
        setTimeout(() => {
          currentCard.classList.remove('stopped')
          this.animateCycle(index + 1)
        }, this.cycleDelay)
      }, this.animationDuration)
    },
    stopAnimation(index) {
      const cards = document.querySelectorAll('.levelhome_box-img_inner')
      cards[index].classList.remove('animated')
      cards[index].classList.add('stopped')
    },
  },
}
</script>
